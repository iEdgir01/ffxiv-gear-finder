/**
 * Build `js/itemMeta.js` — LevelEquip + ClassJobCategory + ItemUICategory names for
 * every Teamcraft recipe result id, so the finder can filter by **equip level** before
 * calling XIVAPI (small fetch set).
 *
 * Sources: Teamcraft recipes.json + xivapi/ffxiv-datamining CSVs (en).
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, '..');
const OUT = path.join(ROOT, 'js', 'itemMeta.js');

const TEAMCRAFT =
  'https://raw.githubusercontent.com/ffxiv-teamcraft/ffxiv-teamcraft/master/libs/data/src/lib/json/';
const BASE = 'https://raw.githubusercontent.com/xivapi/ffxiv-datamining/master/csv/en/';

async function fetchText(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Fetch failed ${res.status}: ${url}`);
  return await res.text();
}

function csvLines(text) {
  return text.split(/\r?\n/).filter(Boolean);
}

/** Minimal CSV line split supporting quoted fields. */
function parseCsvLine(line) {
  const out = [];
  let cur = '';
  let inQ = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (c === '"') {
      inQ = !inQ;
      continue;
    }
    if (c === ',' && !inQ) {
      out.push(cur);
      cur = '';
      continue;
    }
    cur += c;
  }
  out.push(cur);
  return out;
}

function headerIndex(headerRow) {
  const idx = new Map();
  const parts = parseCsvLine(headerRow);
  for (let i = 0; i < parts.length; i++) idx.set(parts[i], i);
  return idx;
}

function parseSheet(text) {
  const lines = csvLines(text);
  const header = lines[0];
  const hIdx = headerIndex(header);
  const byId = new Map();
  for (let i = 1; i < lines.length; i++) {
    const r = parseCsvLine(lines[i]);
    const first = r[0]?.trim();
    if (/^\d+$/.test(first)) {
      byId.set(Number(first), r);
    }
  }
  return { header, hIdx, byId };
}

function toNum(v) {
  const n = Number(v);
  return Number.isFinite(n) ? n : 0;
}

async function main() {
  const [recipesRaw, itemText, cjcText, uiText] = await Promise.all([
    fetch(TEAMCRAFT + 'recipes.json').then(r => {
      if (!r.ok) throw new Error('recipes.json');
      return r.json();
    }),
    fetchText(BASE + 'Item.csv'),
    fetchText(BASE + 'ClassJobCategory.csv'),
    fetchText(BASE + 'ItemUICategory.csv'),
  ]);

  const itemSheet = parseSheet(itemText);
  const cjcSheet = parseSheet(cjcText);
  const uiSheet = parseSheet(uiText);

  const itemIdx = itemSheet.hIdx;
  const cjcIdx = cjcSheet.hIdx;
  const uiIdx = uiSheet.hIdx;

  const ixLe = itemIdx.get('LevelEquip');
  const ixCjc = itemIdx.get('ClassJobCategory');
  const ixUi = itemIdx.get('ItemUICategory');
  if (ixLe === undefined || ixCjc === undefined || ixUi === undefined) {
    throw new Error('Item.csv missing expected columns');
  }

  const cjcName = id => {
    const row = cjcSheet.byId.get(id);
    if (!row) return '';
    const j = cjcIdx.get('Name');
    return String(row[j] ?? '').trim();
  };
  const uiName = id => {
    const row = uiSheet.byId.get(id);
    if (!row) return '';
    const j = uiIdx.get('Name');
    return String(row[j] ?? '').trim();
  };

  const resultIds = new Set();
  for (const recipe of recipesRaw) {
    const rid = recipe?.result;
    if (typeof rid === 'number' && rid > 0) resultIds.add(rid);
  }

  const meta = {};
  let missing = 0;
  for (const id of resultIds) {
    const row = itemSheet.byId.get(id);
    if (!row) {
      missing++;
      continue;
    }
    const le = toNum(row[ixLe]);
    const cjcId = toNum(row[ixCjc]);
    const uiCatId = toNum(row[ixUi]);
    const cjc = cjcName(cjcId);
    const ui = uiName(uiCatId);
    meta[String(id)] = { le, cjc, ui };
  }

  const stamp = new Date().toISOString();
  const out = `// AUTO-GENERATED by scripts/build-item-meta.mjs — do not hand edit.
// Generated: ${stamp}
// Recipe result ids: ${resultIds.size}, meta rows: ${Object.keys(meta).length}, missing Item rows: ${missing}

export const ITEM_META = ${JSON.stringify(meta, null, 0)};
`;

  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, out, 'utf8');
  console.info('[build-item-meta] Wrote', OUT, 'keys:', Object.keys(meta).length);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
